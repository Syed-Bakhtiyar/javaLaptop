    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        jTextArea1.setText("");
        int count=0;
        long rd;        
        try {
                    
                    try(RandomAccessFile rand = new RandomAccessFile(Filename+".dat", "rw")){
                    rand.seek(0);
                        while (true) {                            
                           rd=rand.readLong();
                            jTextArea1.setText(jTextArea1.getText()+rd+"\n");
                    count++;
                        }
                }
            
                } catch (Exception e) {
                    if(count==0)
                    
                    jTextArea1.setText(""+count);
            
                }

    }//GEN-LAST:event_jButton4ActionPerformed

