    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Filename == null||Filename=="none"){
             JOptionPane.showMessageDialog(null, "Type Date "); 
        }
        else{
       
                try {
                    data = Long.parseLong(jTextField1.getText());
                    try(RandomAccessFile rand = new RandomAccessFile(Filename+".dat", "rw")){
                    rand.seek(rand.length());
                    rand.writeLong(data);
                    rand.close();
                }
            
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "type only number");
                    jTextField1.setText("");
            
                }
        }
        jTextField1.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

